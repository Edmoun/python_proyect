"""
Como hacer comentarios de una y varias lineas en python. 

Con el signo de numero(#) podemos hacer un comentario de una linea; 
y con (""" """ y asi uno de varias)

"""

numero_entero = 1
print(numero_entero)

numero_decimal = 9.8
print(numero_decimal)

numero_entero += 2
print(numero_entero)

resta = 25
resta -= 10
print(resta)

multiplicar = 10
multiplicar *= numero_entero
print(multiplicar)

dividir = 100
dividir /= 2
print(dividir)

potencia = 10**2
print(potencia)

